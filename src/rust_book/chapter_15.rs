use std::cell::RefCell;
use List3::{Cons, Nil};
#[derive(Debug)]
enum List3 {
    Cons(i32, RefCell<Rc<List3>>),
    Nil,
}

impl List3 {
    fn tail(&self) -> Option<&RefCell<Rc<List3>>> {
        match self {
            Cons(_, item) => Some(item),
            Nil => None,
        }
    }
}

